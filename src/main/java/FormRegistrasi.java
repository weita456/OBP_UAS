    private void btnregistrasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrasiActionPerformed
        // TODO add your handling code here:
        try {
            if (txtuser.getText().equals("")|| txtpass.getPassword().equals("") ||
                    txtnama.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Data Tidak Boleh Kosong" , "Pesan" , JOptionPane.ERROR_MESSAGE);
                hapuslayar();
            }else {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/tbjava","root", "");
                st = con.createStatement();
                String simpan = "insert into tblogin values('" +txtuser.getText()+ "','" + String.valueOf(txtpass.getPassword())+"','" +txtnama.getText()+"')";
                st = con.createStatement();
                int SA = st.executeUpdate(simpan);
                JOptionPane.showMessageDialog(this, "Registrasi Berhasil");
                this.setVisible(false);
                new FormLogin(this, rootPaneCheckingEnabled).setVisible(true);
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(this,"Akun Sudah Dibuat/Duplikat Akun","Pesan",
                    JOptionPane.WARNING_MESSAGE);
            hapuslayar();
        }
    }//GEN-LAST:event_btnregistrasiActionPerformed

